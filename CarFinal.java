public class CarFinal {
    final int noOfWheels;
    final String model;
    final String engineInLiters;

    public CarFinal(int noOfWheels, String model,String engineInLiters){
        this.noOfWheels = noOfWheels;
        this.model = model;
        this.engineInLiters = engineInLiters;
    }
    
    
}
